    <h3 id="whyneed$$">Why jQuery was Needed</h3>

    <p>John Resig created jQuery with the goal of simplifying and streamlining the process of writing JavaScript code, for web
        developers. This library was designed to offer a speedy and user-friendly interface for managing DOM, events, AJAX requests
        and animations. Its introduction brought about a transformation in web development by providing a yet straightforward API.
        As a result, jQuery quickly became one of the used and popular JavaScript <br>libraries. Its influence has also played a role,
        in the development of JavaScript libraries and frameworks contributing to the growth of dynamic web applications.
    </p>

